# ── 主入口 ────────────────────────────────────────────────────────────
def _parse_id(raw: str) -> int:
    """把命令行 id 参数解析成 int；非法值给友好提示而非 ValueError traceback（U1）。"""
    try:
        return int(raw)
    except (ValueError, TypeError):
        print(f"❌ 无效的钩子 id: '{raw}'（应为数字，如 1、2、3）")
        print("   用 `python3 manage.py list` 查看现有钩子编号")
        sys.exit(1)


def main():
    args = sys.argv[1:]
    if not args:
        print(__doc__)
        sys.exit(0)

    cmd, rest = args[0], args[1:]

    if   cmd == "install": cmd_install()
    elif cmd == "list":    cmd_list()
    elif cmd == "show":
        if not rest: print("用法: manage.py show <id>"); sys.exit(1)
        cmd_show(_parse_id(rest[0]))
    elif cmd == "add":     cmd_add(rest)
    elif cmd == "edit":
        if not rest: print("用法: manage.py edit <id> [选项]"); sys.exit(1)
        cmd_edit(_parse_id(rest[0]), rest[1:])
    elif cmd == "enable":
        if not rest: print("用法: manage.py enable <id>"); sys.exit(1)
        cmd_enable(_parse_id(rest[0]))
    elif cmd == "disable":
        if not rest: print("用法: manage.py disable <id>"); sys.exit(1)
        cmd_disable(_parse_id(rest[0]))
    elif cmd == "delete":
        if not rest: print("用法: manage.py delete <id>"); sys.exit(1)
        force = "--force" in rest
        pos = next((x for x in rest if not x.startswith("--")), None)
        if pos is None: print("用法: manage.py delete <id> [--force]"); sys.exit(1)
        cmd_delete(_parse_id(pos), force=force)
    elif cmd == "run":
        hook_id = _parse_id(rest[0]) if rest and rest[0].lstrip("-").isdigit() else None
        cmd_run(hook_id)
    elif cmd == "status":  cmd_status()
    else:
        print(f"未知命令: {cmd}\n")
        print(__doc__)
        sys.exit(1)


if __name__ == "__main__":
    main()
